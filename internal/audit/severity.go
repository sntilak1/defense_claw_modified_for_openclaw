package audit

import "strings"

// SeverityRank maps a severity string to an integer for comparison.
// CRITICAL=5, HIGH=4, MEDIUM=3, LOW=2, INFO=1, unknown=0.
func SeverityRank(s string) int {
	switch strings.ToUpper(s) {
	case "CRITICAL":
		return 5
	case "HIGH":
		return 4
	case "MEDIUM":
		return 3
	case "LOW":
		return 2
	case "INFO":
		return 1
	default:
		return 0
	}
}
